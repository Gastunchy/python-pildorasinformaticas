# Modularizacion

'''
Una modularizacion se refiere a la práctica de dividir un programa en partes más pequeñas y manejables, llamadas módulos. Cada módulo agrupa elementos relacionados, como clases, funciones y datos, que juntos cumplen con una función específica del programa. La modularización mejora la organización del código, facilita su mantenimiento y promueve la reutilización.
'''

# Encapsulacion

'''
La encapsulación es una técnica de la POO que agrupa datos y métodos dentro de una clase y controla el acceso a ellos para proteger la integridad del objeto. En Python, se utilizan convenciones de nomenclatura para indicar la visibilidad de los atributos y métodos, y se implementan getters y setters para gestionar el acceso y la modificación de los atributos privados. Esto promueve un código más seguro, mantenible y modular.

'''

# Nomenclatura del punto

'''
La nomenclatura del punto es una técnica esencial en la programación orientada a objetos que se utiliza para acceder a los atributos y métodos de un objeto, así como para trabajar con módulos y paquetes. Su uso promueve la claridad, la legibilidad y la organización modular del código, facilitando el desarrollo y mantenimiento de aplicaciones complejas.
'''