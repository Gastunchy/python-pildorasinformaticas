# Bucles

# Tipos de bucles
'''
-> Determinados: Sabemos cuantas veces se va a ejecutar el codigo que hay en su interior
-> Indeterminados: no sabemos cuantas veces se repetira o sera necesario repetir el bucle
'''

# Determinados

'''
Bucle 'for'
sintaxis: 'for' variable 'in' elemento a recorrer( lista, tubla, cadena de texto, etc):'''

# Imprime un Hola seguido del elemento de la lista y se repite segun la cantidad de elementos que tiene la lista
for i in [1, 2, 3]:
    print(f"Hola {i}")

for i in ["Nombre", "Farias"]:
    print(i)

for i in "Hola caracola":
    print(i)

