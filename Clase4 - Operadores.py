'''
Operadores aritmeticos:

suma: +
resta: -
division: /
multiplicacion: *
modulo: % (resto de una division inexacta)
division entera: //

operadores de comparacion:

igual que: ==
diferente que: !=
mayor que: >
menor que: <
mayor o igual que: >=
menor o igual que: <=

Operadores logicos:

AND, OR, NOT

Asignacion:

igual: =
incremento: +=
Decremento: -=
otros: *=, /=, %=, **=, //=

Especiales:
 IS, IN, IS NOT, NOT IN
'''

print ("Esto es un numero", 3)
print ("Esto es texto")

numero1 = 10
numero2 = 3

#Operadores aritmeticos

suma = numero1 + numero2
resta = numero1 - numero2
division = numero1 / numero2
multiplicacion = numero1 * numero2
modulo = numero1 % numero2
exponente = numero1 ** numero2
division_e = numero1 // numero2


print (f"Numero {numero1} y {numero2} ")
print (f"Suma= {suma}")
print (f"resta= {resta}")
print (f"division= {division}")
print (f"multiplicacion= {multiplicacion}")
print (f"modulo={modulo}")
print (f"exponente={exponente}")
print (f"division_e={division_e}")
